			In the 1990s, many immigrants bypassed established gateways like Los Angeles, New York, Chicago, and Miami to create new immigrant destinations across the US. In a 2011 Social Science Research article, Dr. Julie Park (core faculty in the Asian American Studies Program and assistant professor in the Department of Sociology) and her co-author John Iceland examine how residential segregation and incorporation might differ between established gateways and new destinations among the 150 largest metropolitan areas. They find that segregation levels were consistently lower in new destinations but they significantly increased during the 1990s. Results indicate that Asian residential patterns in new destinations are similar to what was observed in established gateways while Latinos in new destinations may experience slower or hindered progress of incorporation.</div>
